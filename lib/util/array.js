/**
 * Array.prototype.random
 * Return a random elements from an array.
 * this one is mine :D
 */
if(!Array.prototype.random){
  Array.prototype.random = function(){
    return this.splice(Math.floor(Math.random() * this.length), 1)[0]
  }
}


